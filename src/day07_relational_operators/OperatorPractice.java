package day07_relational_operators;

public class OperatorPractice {
    public static void main(String[] args) {
        int num = 10;
        num++;//post increment
        System.out.println(num);

        num--;
        System.out.println(num);
        System.out.println(num++);
        System.out.println(num++);
        System.out.println(++num);
        System.out.println(num++);



    }
}

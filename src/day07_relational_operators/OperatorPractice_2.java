public class OperatorPractice_2 {
    public static void main(String[] args) {
        int a=40;
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
    }
}

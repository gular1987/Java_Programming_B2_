package day07_relational_operators;

public class OperatorPractice_3 {
    public static void main(String[] args) {
        int h=5;
        int p=h;
        h++;
        System.out.println("h"+h);
        System.out.println("p"+p);
        int k=h++;
        System.out.println("h"+h);
        System.out.println("k"+k);
        int g=++h;
        System.out.println("h"+h);
        System.out.println("g"+g);



    }
}
